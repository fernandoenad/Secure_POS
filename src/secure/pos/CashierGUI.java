    public void new_invoice(){
        DefaultTableModel model = (DefaultTableModel) itemsTBL.getModel();
        model.setRowCount(0);
        barcodeTF.setText("");
        barcodeTF.grabFocus();
        close_saleBTN.setEnabled(false);
        total_costLBL.setText("0.00");
        amt_paidTF.setText("0.00");
        changeLBL.setText("0.00");
        
        this.newInvoice = new Invoice(0, 0, 0.00, 0.00, 0.00, this.loggedUser.getUser_id(), "Pending", "");
        
        if(invoiceDAOImpl.create(this.newInvoice) == true){
            this.newInvoice = invoiceDAOImpl.read_last_insert(this.loggedUser.getUser_id());
            loggedUsernameLBL.setText("Cashier: " + this.loggedUser.getUsername());
            invoice_noLBL.setText("Invoice No.: " + this.newInvoice.getInvoice_id());
            total_quantityLBL.setText("Total Quantity: " + this.newInvoice.getTotal_quantity());
        } else {
            JOptionPane.showMessageDialog(null, "Error was encountered!", "Message", JOptionPane.WARNING_MESSAGE);
        }
    }

