    private void amt_paidTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_amt_paidTFActionPerformed
        // TODO add your handling code here:
        double total_cost = Double.parseDouble(total_costLBL.getText());
        double amt_paid = Double.parseDouble(amt_paidTF.getText());
        amt_paidTF.setText(String.format("%.2f", amt_paid));
        double change = amt_paid - total_cost;
        changeLBL.setText(String.format("%.2f", change));
        
        if(change > 0){
            JOptionPane.showMessageDialog(null, "Printing receipt... Click OK to start a new transaction!", "Message", JOptionPane.INFORMATION_MESSAGE);
            this.newInvoice.setTotal_cost(total_cost);
            this.newInvoice.setAmt_paid(amt_paid);
            this.newInvoice.setChange(change);
            this.newInvoice.setStatus("Completed");

            if(invoiceDAOImpl.update(this.newInvoice.getInvoice_id(), this.newInvoice)){
                DefaultTableModel model = (DefaultTableModel) itemsTBL.getModel();

                for (int row = 0; row < model.getRowCount(); row++) {
                    for (int col = 0; col < model.getColumnCount(); col++) {
                        int product_id = Integer.parseInt(model.getValueAt(row, 0).toString()); 
                        int quantity = Integer.parseInt(model.getValueAt(row, 2).toString()); 
                        Item item = new Item(0, this.newInvoice.getInvoice_id(), product_id, quantity);
                        
                        if(itemDAOImpl.create(item) == false){
                            JOptionPane.showMessageDialog(null, "Error encountered!", "Message", JOptionPane.WARNING_MESSAGE);
                        }
                    }
                }
                
                new_invoice();
            } else {
                JOptionPane.showMessageDialog(null, "Error encountered!", "Message", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Insufficient Payment!", "Message", JOptionPane.WARNING_MESSAGE);
            amt_paidTF.selectAll();
        }
        
    }//GEN-LAST:event_amt_paidTFActionPerformed

