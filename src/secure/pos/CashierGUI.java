    private void barcodeTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barcodeTFActionPerformed
        // TODO add your handling code here:
        int product_id = Integer.parseInt(barcodeTF.getText());
        Product product = productDAOImpl.read_one(product_id);
        
        if(product != null){
            
            DefaultTableModel model = (DefaultTableModel) itemsTBL.getModel();
            
            model.addRow(new Object[]{
                product.getProduct_id(),
                product.getName(),
                1,
                String.format("%.2f", product.getPrice())
            });
            
            total_quantityLBL.setText("Quantity: " + model.getRowCount());
            double total_cost = Double.parseDouble(total_costLBL.getText());
            total_costLBL.setText(String.format("%.2f", total_cost + product.getPrice()));
            close_saleBTN.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Product not found!", "Message", JOptionPane.WARNING_MESSAGE);
        }
        
        barcodeTF.setText("");
    }//GEN-LAST:event_barcodeTFActionPerformed

